package com.javarush.task.task01.task0102;

public class Solution {
    public static void main(String[] args) {
        // помещаем в переменную текст, который нужно вывести на экран
        String text = "Элли самая красивая";

        // выводим на экран значение переменной 5 раз (повторяем команду
        // вывода 5 раз), каждый раз с новой строки
        System.out.println(text);
        System.out.println(text);
        System.out.println(text);
        System.out.println(text);
        System.out.println(text);
    }
}
