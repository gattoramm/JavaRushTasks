package com.javarush.task.task02.task0207;

public class Solution {
    public static void main(String[] args) {
        // объявляем переменную a типа int и сразу же в строке объявления
        // присваиваем ей значение
        int a = 10;
        // объявляем переменную b типа int и сразу же в строке объявления
        // присваиваем ей значение
        int b = 20;
        // выводим на экран значение суммы переменных a и b
        System.out.println(a + b);
        // выводим на экран значение произведения переменных a и b
        System.out.println(a * b);
    }
}
